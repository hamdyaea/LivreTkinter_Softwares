import tkinter as tk

# Créer la fenêtre principale
window = tk.Tk()

# Créer une entrée de texte
entry = tk.Entry(window)

# Ajouter l'entrée de texte à la fenêtre
entry.pack()

# Afficher la fenêtre
window.mainloop()
#Lorsque vous exécutez ce code, une zone de texte vide s'affiche dans la fenêtre.
